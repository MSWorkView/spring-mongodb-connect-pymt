package com.example.firstproject.domain;


public class Payee {
	private String beneficiaryAccountCountry;
	private String beneficiaryAccountNumber;
	private String beneficiaryBankCode;
	private String beneficiaryName;
	private String beneficiaryNickName;
	public String getBeneficiaryAccountCountry() {
		return beneficiaryAccountCountry;
	}
	public void setBeneficiaryAccountCountry(String beneficiaryAccountCountry) {
		this.beneficiaryAccountCountry = beneficiaryAccountCountry;
	}
	public String getBeneficiaryAccountNumber() {
		return beneficiaryAccountNumber;
	}
	public void setBeneficiaryAccountNumber(String beneficiaryAccountNumber) {
		this.beneficiaryAccountNumber = beneficiaryAccountNumber;
	}
	public String getBeneficiaryBankCode() {
		return beneficiaryBankCode;
	}
	public void setBeneficiaryBankCode(String beneficiaryBankCode) {
		this.beneficiaryBankCode = beneficiaryBankCode;
	}
	public String getBeneficiaryName() {
		return beneficiaryName;
	}
	public void setBeneficiaryName(String beneficiaryName) {
		this.beneficiaryName = beneficiaryName;
	}
	public String getBeneficiaryNickName() {
		return beneficiaryNickName;
	}
	public void setBeneficiaryNickName(String beneficiaryNickName) {
		this.beneficiaryNickName = beneficiaryNickName;
	}
	
	
	
	
}
